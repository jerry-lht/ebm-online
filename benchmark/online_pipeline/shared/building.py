"""Shared dataset-build utilities for online pipeline benchmarks."""

from __future__ import annotations

import base64
import hashlib
import json
import ssl
import urllib.request
from datetime import datetime, timezone
from pathlib import Path
from typing import Any, Callable

from benchmark.online_pipeline.shared.jsonl import write_jsonl
from benchmark.online_pipeline.shared.report_utils import write_json


BUILDER_VERSION = "online-pipeline-builder-v2"
DEFAULT_SEED = "42"
ROOT = Path(__file__).resolve().parents[1]
RAW_DATA_DIR = ROOT / "raw_data"


def write_dataset_artifacts(
    *,
    module: str,
    source: str,
    dataset_name: str,
    records: list[dict[str, Any]],
    source_manifest: dict[str, Any],
    splits: dict[str, list[dict[str, Any]]],
    split_strategy: str,
    seed: str,
    sample_size: int | None,
    source_record_count: int,
    dataset_analysis: dict[str, Any] | None = None,
) -> dict[str, Any]:
    dataset_dir = ROOT / module / "datasets" / dataset_name
    write_split(dataset_dir, records)
    for split_name, split_records in splits.items():
        write_split(dataset_dir / "splits" / split_name, split_records)

    split_manifest = {
        "builder_version": BUILDER_VERSION,
        "seed": seed,
        "module": module,
        "dataset_name": dataset_name,
        "source": source,
        "sample_size": sample_size,
        "source_record_count": source_record_count,
        "selected_count": len(records),
        "split_strategy": split_strategy,
        "splits": {
            split_name: {
                "count": len(split_records),
                "instance_ids": [record["instance"]["instance_id"] for record in split_records],
            }
            for split_name, split_records in {"all": records, **splits}.items()
        },
    }
    build_manifest = {
        "builder_version": BUILDER_VERSION,
        "built_at": datetime.now(timezone.utc).isoformat(),
        "module": module,
        "dataset_name": dataset_name,
        "source": source,
        "sample_size": sample_size,
        "source_record_count": source_record_count,
        "selected_count": len(records),
        "seed": seed,
        "source_manifest_sha256": sha256_json(source_manifest),
        "split_manifest_sha256": sha256_json(split_manifest),
    }
    write_json(dataset_dir / "source_manifest.json", source_manifest)
    write_json(dataset_dir / "split_manifest.json", split_manifest)
    write_json(dataset_dir / "build_manifest.json", build_manifest)
    write_schema(dataset_dir / "schema.md", module=module)
    if dataset_analysis is not None:
        write_json(dataset_dir / "dataset_analysis.json", dataset_analysis)
    return {
        "dataset_dir": str(dataset_dir),
        "source_manifest": source_manifest,
        "build_manifest": build_manifest,
        "split_manifest": split_manifest,
    }


def write_split(split_dir: Path, records: list[dict[str, Any]]) -> None:
    write_jsonl(split_dir / "instances.jsonl", [record["instance"] for record in records], sort_keys=False)
    write_jsonl(split_dir / "gold.jsonl", [record["gold"] for record in records], sort_keys=False)


def write_schema(path: Path, *, module: str) -> None:
    text = (
        f"# {module} benchmark dataset\n\n"
        "Generated by `benchmark/online_pipeline/benchmark.py build`.\n\n"
        "- `instances.jsonl`: module inputs\n"
        "- `gold.jsonl`: benchmark gold labels\n"
        "- `source_manifest.json`: raw source metadata\n"
        "- `build_manifest.json`: reproducible build metadata\n"
        "- `split_manifest.json`: split IDs and strategy\n"
    )
    path.write_text(text, encoding="utf-8")


def select_records(
    records: list[dict[str, Any]],
    *,
    sample_size: int | None,
    seed: str,
    module: str,
) -> list[dict[str, Any]]:
    if sample_size is None:
        return records
    return stable_sample(records, sample_size=sample_size, seed=seed, module=module)


def stable_sample(records: list[dict[str, Any]], *, sample_size: int, seed: str, module: str) -> list[dict[str, Any]]:
    return stable_order(records, seed=seed, module=module)[: min(sample_size, len(records))]


def stable_order(records: list[dict[str, Any]], *, seed: str, module: str) -> list[dict[str, Any]]:
    return sorted(records, key=lambda record: stable_key(seed=seed, module=module, instance_id=record["source_id"]))


def stable_key(*, seed: str, module: str, instance_id: str) -> str:
    return hashlib.sha256(f"{seed}:{module}:{instance_id}".encode("utf-8")).hexdigest()


def normalize_split_name(value: str) -> str:
    clean = value.strip().lower()
    if clean in {"validation", "valid", "val", "dev"}:
        return "dev"
    if clean in {"train", "training"}:
        return "dev"
    if clean in {"test", "testing"}:
        return "test"
    if clean == "smoke":
        return "smoke"
    return "test"


def download_or_resolve_source(
    *,
    source: str,
    source_url: str,
    fallback: Callable[[Path], None] | None = None,
) -> Path:
    source_path = Path(source_url)
    if source_path.exists():
        return source_path
    source_dir = RAW_DATA_DIR / source
    source_dir.mkdir(parents=True, exist_ok=True)
    suffix = Path(source_url.split("?")[0]).suffix or ".jsonl"
    target = source_dir / f"{source}{suffix}"
    try:
        request = urllib.request.Request(source_url, headers={"User-Agent": "EBM-Online-Benchmark/1.0"})
        with urllib.request.urlopen(request, context=ssl_context(), timeout=60) as response:
            target.write_bytes(response.read())
    except Exception:
        if fallback is None:
            raise
        fallback(target)
    return target


def download_github_base64_blob(*, blob_url: str, target: Path) -> None:
    request = urllib.request.Request(blob_url, headers={"User-Agent": "EBM-Online-Benchmark/1.0"})
    with urllib.request.urlopen(request, context=ssl_context(), timeout=120) as response:
        payload = json.loads(response.read().decode("utf-8"))
    if payload.get("encoding") != "base64":
        raise RuntimeError("Unexpected GitHub blob encoding")
    target.write_bytes(base64.b64decode("".join(str(payload["content"]).splitlines())))


def ssl_context() -> ssl.SSLContext:
    try:
        import certifi
    except ImportError:
        return ssl.create_default_context()
    return ssl.create_default_context(cafile=certifi.where())


def source_manifest(
    *,
    source: str,
    records: list[dict[str, Any]],
    source_url: str | None,
    raw_sha256: str | None,
    extra: dict[str, Any] | None = None,
) -> dict[str, Any]:
    manifest = {
        "source": source,
        "source_url": source_url,
        "raw_sha256": raw_sha256,
        "record_count": len(records),
        "builder_version": BUILDER_VERSION,
        "is_builtin_smoke": source == "builtin_smoke",
    }
    if extra:
        manifest.update(extra)
    return manifest


def sha256_json(value: Any) -> str:
    return hashlib.sha256(json.dumps(value, ensure_ascii=False, sort_keys=True).encode("utf-8")).hexdigest()


def sha256_file(path: Path) -> str:
    digest = hashlib.sha256()
    with path.open("rb") as handle:
        for chunk in iter(lambda: handle.read(1024 * 1024), b""):
            digest.update(chunk)
    return digest.hexdigest()


def slot_list(value: Any) -> list[str]:
    if value is None:
        return []
    if isinstance(value, list):
        return [str(item).strip() for item in value if str(item).strip()]
    text = str(value).strip()
    if not text:
        return []
    try:
        parsed = json.loads(text)
    except json.JSONDecodeError:
        return [text]
    if isinstance(parsed, list):
        return [str(item).strip() for item in parsed if str(item).strip()]
    return [text]


def length_bucket(text: str) -> str:
    length = len(text.split())
    if length < 15:
        return "short"
    if length < 30:
        return "medium"
    return "long"
